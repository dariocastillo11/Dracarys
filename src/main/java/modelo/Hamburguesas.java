
package modelo;

public class Hamburguesas {
    final int  hamburBase=500;
    private int lechuga=0;
    private int huevo=100;
    private int lactonesa=0;
    private int repollo=0;
    private int tomate=0;
    private int jamon=100;
    private int chedar=100;
   

  

    public Hamburguesas() {
    }

   public int getHamburBase() {
        return hamburBase;
    }

    
 

    public int getLechuga() {
        return lechuga;
    }

    public void setLechuga(int lechuga) {
        this.lechuga = lechuga;
    }

    public int getHuevo() {
        return huevo;
    }

    public void setHuevo(int huevo) {
        this.huevo = huevo;
    }

    public int getLactonesa() {
        return lactonesa;
    }

    public void setLactonesa(int lactonesa) {
        this.lactonesa = lactonesa;
    }

    public int getRepollo() {
        return repollo;
    }

    public void setRepollo(int repollo) {
        this.repollo = repollo;
    }

    public int getTomate() {
        return tomate;
    }

    public void setTomate(int tomate) {
        this.tomate = tomate;
    }

    public int getJamon() {
        return jamon;
    }

    public void setJamon(int jamon) {
        this.jamon = jamon;
    }

    public int getChedar() {
        return chedar;
    }

    public void setChedar(int chedar) {
        this.chedar = chedar;
    }
   
   

    
   
  
    
}
